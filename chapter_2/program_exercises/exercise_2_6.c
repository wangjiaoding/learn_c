#include <stdio.h>

int main(void)
{
    int toes = 10;
    int toes_double = toes * 2;
    int toes_square = toes * toes;

    printf("toes = %d\n", toes);
    printf("The double of toes = %d\n", toes_double);
    printf("The square of toes = %d\n", toes_square);

    return 0;
}
